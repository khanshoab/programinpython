# break statement

for i in range(10):
    if (i==3):
        break
    print(i)
print("remaining code")

for i in range(10):
    print(i)
    if (i==3):
        break
print("remaining code")

# continue statement

for i in range(10):
    if (i==3):
        continue
    print(i)
print("remaining code")

